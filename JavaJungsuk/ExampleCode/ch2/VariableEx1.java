package Java의정석.ch2;

public class VariableEx1 {
    public static void main(String[] args) {
        int year = 0;
        int age = 21;

        System.out.println(year);
        System.out.println(age);

        year += 2000;
        age += 1;
        System.out.println(year);
        System.out.println(age);
    }
}
